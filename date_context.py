"""
date_context.py — ฉีด "วันที่/เวลาปัจจุบันจริง" เข้า prompt ของทุก AI Agent ในระบบ
(LLM ไม่มีนาฬิกาในตัว ถ้าไม่บอกวันที่จริงให้ มันจะเดา/จำวันที่จากข้อมูลฝึกตัวเองมาตอบ ทำให้ผิดเสมอ)
"""
from datetime import datetime

_TH_WEEKDAYS = ["จันทร์", "อังคาร", "พุธ", "พฤหัสบดี", "ศุกร์", "เสาร์", "อาทิตย์"]
_TH_MONTHS = ["", "มกราคม", "กุมภาพันธ์", "มีนาคม", "เมษายน", "พฤษภาคม", "มิถุนายน",
              "กรกฎาคม", "สิงหาคม", "กันยายน", "ตุลาคม", "พฤศจิกายน", "ธันวาคม"]

def current_datetime_context_th() -> str:
    """คืน block ข้อความสำหรับฉีดเข้า system/agent prompt บอกวันที่-เวลาจริง ณ ขณะนี้
    ต้องเรียกใหม่ทุกครั้งที่สร้าง prompt (ห้าม cache ไว้นาน) เพื่อให้ตรงเวลาจริงเสมอ"""
    now = datetime.now()
    weekday_th = _TH_WEEKDAYS[now.weekday()]
    month_th = _TH_MONTHS[now.month]
    year_be = now.year + 543
    return (
        "## วันที่และเวลาปัจจุบัน (ข้อเท็จจริง — ใช้อ้างอิงเสมอ ห้ามเดาหรือใช้ความจำเก่า)\n"
        f"ขณะนี้คือวัน{weekday_th}ที่ {now.day} {month_th} พ.ศ. {year_be} "
        f"(ค.ศ. {now.year}) เวลา {now.strftime('%H:%M')} น.\n"
        "ถ้าถูกถามเรื่องวัน/เดือน/ปี/เวลาปัจจุบัน ให้ตอบจากข้อมูลนี้เท่านั้น ห้ามคำนวณหรือเดาเอง"
    )
