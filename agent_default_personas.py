def get_agent_role_desc(aid, custom_personas=None, fallback_p=""):
    """ลำดับความสำคัญ: Custom Persona ของผู้ใช้ > Default Persona เชิงลึก > 'p' สั้น ๆ (fallback)"""
    if custom_personas:
        custom_p = (custom_personas.get(aid, "") or "").strip()
        if custom_p:
            return custom_p
    if aid in AGENT_DEFAULT_PERSONAS:
        return AGENT_DEFAULT_PERSONAS[aid]
    return fallback_p
